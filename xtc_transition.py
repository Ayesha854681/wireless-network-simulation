def animate(frame):
    remove_count = len(edges_to_remove)
    add_count = len(edges_to_add)
    if frame < remove_count:
        edge = edges_to_remove[frame]
        if edge in removing_lines:
            removing_lines[edge].remove()
            del removing_lines[edge]
    elif frame < remove_count + add_count:
        i, j = edges_to_add[frame - remove_count]
        line, = ax.plot([node_x_positions[i], node_x_positions[j]], [node_y_positions[i], node_y_positions[j]],
                        color='#00ff00', alpha=0.6, linewidth=0.8)
        adding_lines.append(line)
    elif frame == remove_count + add_count:
        for line in adding_lines:
            line.set_color('#cfcfcf')
            line.set_alpha(0.35)
            line.set_linewidth(0.6)
    return list(removing_lines.values()) + adding_lines

